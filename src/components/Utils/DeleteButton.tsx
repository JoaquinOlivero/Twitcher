import { useState } from "react"

type deleteButtonProps = {
    deleteFunc: () => Promise<void>
}

const DeleteButton = ({ deleteFunc }: deleteButtonProps) => {
    const [waiting, setWaiting] = useState<boolean>(false)


    const handleOnClick = async () => {
        setWaiting(true)
        await deleteFunc()
        setWaiting(false)
    }

    return (
        <div onClick={() => handleOnClick()} className={`flex justify-center items-center bg-red-700 px-2 py-1 rounded font-semibold tracking-wider capitalize cursor-pointer transition hover:bg-red-600 ${waiting && 'pointer-events-none '}`}>
            {waiting ?
                <div className="flex justify-center items-center h-5 w-5">
                    <svg className="animate-spin h-5 w-5 text-white" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
                        <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
                        <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
                    </svg>
                </div>
                :
                <button className="font-semibold tracking-wider capitalize cursor-pointer transition hover:bg-red-600">
                    <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={2} stroke="currentColor" className="h-5 w-5">
                        <path strokeLinecap="round" strokeLinejoin="round" d="M14.74 9l-.346 9m-4.788 0L9.26 9m9.968-3.21c.342.052.682.107 1.022.166m-1.022-.165L18.16 19.673a2.25 2.25 0 01-2.244 2.077H8.084a2.25 2.25 0 01-2.244-2.077L4.772 5.79m14.456 0a48.108 48.108 0 00-3.478-.397m-12 .562c.34-.059.68-.114 1.022-.165m0 0a48.11 48.11 0 013.478-.397m7.5 0v-.916c0-1.18-.91-2.164-2.09-2.201a51.964 51.964 0 00-3.32 0c-1.18.037-2.09 1.022-2.09 2.201v.916m7.5 0a48.667 48.667 0 00-7.5 0" />
                    </svg>
                </button>
            }
        </div>
    )
}

export default DeleteButton